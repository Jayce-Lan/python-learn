x = 9;
print("Output #1: {0}".format(x));
# 3的4次方
print("Output #2: {0}".format(3 ** 4)); 
# 将数字转换为整数并进行相除
print("Output #3: {0}".format(int(8.3) / int(2.7)));

# 浮点数，使用 :.xf的格式，可以为浮点数保留小数点后x位数
print("Output #4: {0:.3f}".format(8.3 / 2.7));
print("Output #5: {0:.1f}".format(2.5 * 4.8));
print("Output #6: {0:.2f}".format(8 / float(3)));
print("Output #6: {0:.4f}".format(8.0 / 3));